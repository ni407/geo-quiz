export const convertIdToIsoCode = (id: string): string => {
    const isoCode = countryIsoCodes[id];
    if (!isoCode) {
        throw new Error(`Invalid country ID: ${id}`);
    }
    return isoCode;
};

export const getFlagImageUrl = (id: string): string => {
    const isoCode = convertIdToIsoCode(id);
    return `https://flagcdn.com/256x192/${isoCode.toLowerCase()}.png`;
};
